export const getQuestionsByChapter = (chapterId: string, difficulty?: string): Question[] => {
  let result = questions.filter(q => q.chapterId === chapterId);
  if (difficulty) {
    result = result.filter(q => q.difficulty === difficulty);
  }
  return result;
};

export const getQuestionsByType = (chapterId: string, type: string): Question[] => {
  return questions.filter(q => q.chapterId === chapterId && q.type === type);
};

export const getRandomQuestions = (chapterId: string, count: number, difficulty?: string): Question[] => {
  let pool = questions.filter(q => q.chapterId === chapterId);
  if (difficulty) {
    pool = pool.filter(q => q.difficulty === difficulty);
  }
  const shuffled = pool.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
};

export const getQuestionById = (questionId: string): Question | undefined => {
  return questions.find(q => q.id === questionId);
};
