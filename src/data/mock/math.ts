export const subjects: Subject[] = [mathSubject];

export const getChaptersBySubject = (subjectId: string): Chapter[] => {
  const subject = subjects.find(s => s.id === subjectId);
  return subject?.chapters || [];
};

export const getChapterById = (subjectId: string, chapterId: string): Chapter | undefined => {
  const chapters = getChaptersBySubject(subjectId);
  return chapters.find(c => c.id === chapterId);
};

export const getKnowledgePointsByChapter = (subjectId: string, chapterId: string): KnowledgePoint[] => {
  const chapter = getChapterById(subjectId, chapterId);
  return chapter?.knowledgePoints || [];
};

export const getLearningLogicByChapter = (subjectId: string, chapterId: string): LearningLogic | undefined => {
  const chapter = getChapterById(subjectId, chapterId);
  return chapter?.learningLogic;
};
